Name = "Rushi"
# Positive Slicing
print(Name[0:5])
print(Name[1:5])
print(Name[2:5])
print(Name[3:5])
print(Name[4:5])
print(Name[:5])
print(Name[:])

print("-----------------------------------------------------------------------------")

# Negative Slicing
print(Name[0:-4])
print(Name[0:-3])
print(Name[0:-2])
print(Name[0:-1])
print(Name[:-5])



