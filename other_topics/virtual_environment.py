''' 
The virtual environment in Python refers to creating isolated environments 
for project-specific dependencies and Python interpreters.

A virtual environment is a separate container on your system 
where you install Python packages and dependencies just for one project.

Specially useful when package versions or packages are not compatible with each other.
'''


# Create the Virtual environment
''' python -m venv myenv '''

# Activate the Virtual environment
''' myenv\Scripts\activate '''

# Deactivate the Virtual environment
''' deactivate '''
